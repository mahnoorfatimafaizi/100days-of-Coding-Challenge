function cityCountry(city: string, country: string){
    return `${city}, ${country}`;
}

let cityCountry1 = cityCountry("Karachi", "Pakistan");
let cityCountry2 = cityCountry("Tokyo", "Japan");
let cityCountry3 = cityCountry("Oxford", "United Kingdom");

console.log(cityCountry1);
console.log(cityCountry2);
console.log(cityCountry3);